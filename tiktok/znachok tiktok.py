import turtle
yan = turtle.Pen()
yan.ht()

colors = ['aqua', 'black']
yan.width(5)
for i in range(2):
    yan.fillcolor(colors[i%2])
    yan.begin_fill()
    yan.left(90)
    yan.fd(50)
    yan.left(90)
    yan.circle(100,270)
    yan.fd(150)
    yan.right(135)
    yan.circle(100,45)
    yan.left(90)
    yan.fd(50)
    yan.left(80)
    yan.circle(-105,55)
    yan.left(65)
    yan.fd(50)
    yan.left(90)
    yan.fd(255)
    yan.right(30)
    yan.circle(-50,240)
    yan.end_fill()

    yan.penup()
    yan.fd(10)
    yan.right(90)
    yan.fd(10)
    yan.left(90)
    yan.pendown()